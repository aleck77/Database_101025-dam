-- ============================================================================
-- DROP TABLES (если нужно пересоздать)
-- ============================================================================
DROP TABLE IF EXISTS Printer;
DROP TABLE IF EXISTS Laptop;
DROP TABLE IF EXISTS PC;
DROP TABLE IF EXISTS Product;

-- Вариант A (минимально правим, оставляем имя type)
ALTER TABLE Product DROP CHECK Product_chk_1;
ALTER TABLE Product MODIFY `type` VARCHAR(7) NOT NULL;
ALTER TABLE Product ADD CONSTRAINT Product_chk_1 CHECK (`type` IN ('PC', 'Laptop', 'Printer'));
-- ============================================================================
-- CREATE TABLE: Product
-- Таблица производителей и моделей продуктов
-- ============================================================================
CREATE TABLE Product (
  maker VARCHAR(10) NOT NULL,
  model INT NOT NULL,
  type CHAR(7) NOT NULL CHECK (type IN ('PC', 'Laptop', 'Printer')),
  PRIMARY KEY (model),
  CONSTRAINT unique_model_per_maker UNIQUE (maker, model)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- ============================================================================
-- CREATE TABLE: PC
-- Таблица персональных компьютеров
-- ============================================================================
CREATE TABLE PC (
  code INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  model INT NOT NULL,
  speed INT NOT NULL CHECK (speed > 0),
  ram INT NOT NULL CHECK (ram > 0),
  hd INT NOT NULL CHECK (hd > 0),
  cd VARCHAR(5) NOT NULL,
  price DECIMAL(10, 2) NOT NULL CHECK (price > 0),
  FOREIGN KEY (model) REFERENCES Product(model) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- ============================================================================
-- CREATE TABLE: Laptop
-- Таблица портативных компьютеров (ноутбуков)
-- ============================================================================
CREATE TABLE Laptop (
  code INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  model INT NOT NULL,
  speed INT NOT NULL CHECK (speed > 0),
  ram INT NOT NULL CHECK (ram > 0),
  hd INT NOT NULL CHECK (hd > 0),
  screen DECIMAL(3, 1) NOT NULL CHECK (screen > 0),
  price DECIMAL(10, 2) NOT NULL CHECK (price > 0),
  FOREIGN KEY (model) REFERENCES Product(model) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- ============================================================================
-- CREATE TABLE: Printer
-- Таблица принтеров
-- ============================================================================
CREATE TABLE Printer (
  code INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  model INT NOT NULL,
  color CHAR(1) NOT NULL CHECK (color IN ('y', 'n')),
  type VARCHAR(10) NOT NULL CHECK (type IN ('Laser', 'Jet', 'Matrix')),
  price DECIMAL(10, 2) NOT NULL CHECK (price > 0),
  FOREIGN KEY (model) REFERENCES Product(model) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
